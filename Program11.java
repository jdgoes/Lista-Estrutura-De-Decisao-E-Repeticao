/* Faça um programa que imprima todos os números pares de 1 a 100 
na ordem inversa */


public class Program11 {
    public static void main(String[] args) {
        
        int inversoDeNumerosPar = 0;

        for(inversoDeNumerosPar = 100; inversoDeNumerosPar <= 100 && inversoDeNumerosPar != 0; inversoDeNumerosPar-=2){

            System.out.println(inversoDeNumerosPar);
        }

    }
}
